class Laboratoire:

    def __init__(self):
        self.nom = ""
        self.adresse = ""

    def get_nom(self):
        return self.nom

    def set_nom(self, nom):
        self.nom = nom

    def get_adresse(self):
        return self.adresse

    def set_adresse(self, adresse):
        self.adresse = adresse
